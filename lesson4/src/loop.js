
// Функція для виведення чисел від 0 до 9
function loopFromZeroToNine() {
    for (let i = 0; i < 10; i++) {
      console.log(i);
    }
  }
  
  // Функція для виведення чисел від 100 до 0 з кроком 10
  function loopFromHundredToZero() {
    for (let i = 100; i >= 0; i -= 10) {
      console.log(i);
    }
  }
  
  loopFromZeroToNine();
  loopFromHundredToZero();
  